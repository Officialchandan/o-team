class DetailEntryDataModel {
  String needImg, maxScore, score, scoreRem, paramId, grp, imagePath, grade, paramName, empName;

  DetailEntryDataModel(String needImg, String maxScore, String score, String scoreRem, String paramId, String grp,
      String imagePath, String grade, String paramName, String empName) {
    this.needImg = needImg;
    this.maxScore = maxScore;
    this.score = score;
    this.scoreRem = scoreRem;
    this.paramId = paramId;
    this.grp = grp;
    this.imagePath = imagePath;
    this.grade = grade;
    this.paramName = paramName;
    this.empName = empName;
  }

}